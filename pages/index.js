import Head from 'next/head'
import Image from 'next/image'
import { useEffect, useState } from 'react'
import styles from '../styles/Home.module.css'
import api from './api/api'
import { SpinnerCircularFixed } from 'spinners-react'

export default function Home() {

  const [valuesIPs, setValuesIPs] = useState([])
  const [loading, setLoading] = useState(false);
  var error = 0
  useEffect(() => {
    setLoading(true)
    api.get('/').then((response) => {
      setValuesIPs(response.data.data)
      setLoading(false)
    })
      .catch((err) => {
        console.error('ops! ocorreu um erro :' + err)
        error = err
      });
  }, []);

  setTimeout(function() {
    window.location.reload(1);
  }, 180000); 

  return (
    <div className={styles.container}>
      <Head>
        <title>Máquinas IDEAR</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>

        <h1 className={styles.header}>
          Máquinas IDEAR
        </h1>

        {loading && <SpinnerCircularFixed color='#343941' size={60} className={styles.loading} />}
        <div className={styles.grid}>
          {
            valuesIPs.map(res => {
              return (
                <div key={res.ip_local}>
                  <div  className={styles.card}>
                    <h2>({res.nome})</h2><br />
                    <p> Sistema: {res.sistema} </p><br />
                    <p> Versão do sitema: {res.versão_sistema}</p><br />
                    <p> Processador: {res.processador}</p><br />
                    <p> Memória Ram: {res.memoria_ram}</p><br />
                    <p>IP local: {res.ip_local}</p><br />
                  </div>
                </div>
              )

            })
          }
        </div>
      </main>

      <footer className={styles.footer}>
        <a
          href="https://github.com/a32fred"
          target="_blank"
          rel="noopener noreferrer"
        >
          Powered by A32 ♞
        </a>
      </footer>
    </div>
  )
}
